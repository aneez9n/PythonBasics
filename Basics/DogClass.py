class Dog:
    animal = "dog"

    def __init__(self, breed, color):
        self.breed = breed
        self.color = color


Roger = Dog("Bulldog", "Black")
Shllby = Dog("Husky", "black and white")

print("Qualities of Roger")
print("Roger  is a ", Roger.animal)
print("Roger bread is a ", Roger.breed)
print("Roger color is ", Roger.color)

print("\nQualities of Shllby")
print("Shllby is a ", Shllby.animal)
print("Shllby bread is a ", Shllby.breed)
print("Shllby color is ", Shllby.color)





